<h1>General description</h1>
<p>
	The purpose of the event-biller.com is to help people,
	organizations, or companies to reduce the financial risk
	regarding to the activity of event preparation. In particular it tries to act
	as a crowdfunding service for events : Organizer promote their event project
	and if there are enough attendees or funds then they make the event happen.
</p>
<h1>Detailed description</h1>
<p>
	When accessing the web site, a user can browse
	the existing events or create its own events.
</p>
<p>
	The web site is equiped with an account design pattern which
	allows the user to browse while being identified,
	and within a session.
	Most of activities like event creation and management,
	or ticket buying or booking requires the user being identified.
</p>
<p>
	When booking a ticket, the user only gives a payment authorization.
	The payment will be effectively captured only if the event become confirmed.
	When buying a ticket, the user gives a payment authorization with immediate capture.
</p>
<p>
	All the information entered in the form when requested must be accurate and genuine.
</p>

<h1>Event creation</h1>
<p>
	When a user (acting as an organizer) creates an event, he must be identified. The user specifies
	when the event will occur (happening date),
	when the event will have to be confirmed or cancelled (confirmation date).
	The organizer specifies the condition that are to be met before the confirmation date
	to let the Event-Biller.com web site confirm the event.
	The event is by default planned. It means the organizer
	has not engaged himself that the event will happen.
	Buyer can only book tickets (not buy).
	When the event is confirmed, it engages the organizer to make it happen.
	After an event is confirmed, all claims from buyer regarding this event must be
	directly addressed to the organizer.
</p>

<h1>Event publication</h1>
<p>
	Event-Biller.com can decide on its own to not publish an event
	or to remove an event without informing the user or giving a reason.
</p>

<h1>Event confirmation or cancellation</h1>
<p>
	An event will be confirmed or cancelled by Event-Biller.com
	at the confirmation date if the expected minimum event amount is reached.
	It can be cancelled or confirmed by the owner before the confirmation date.
	
	An event will be confirmed or cancelled before the confirmation date.
	If it is confirmed then the booked tickets will be emitted as bought tickets
	and the matching authorized payment will be captured.
	If an event is cancelled then all booked tickets are cancelled,
	and because no payment have been captured, no user needs to be refunded.
</p>

<h1>Transferring funds to the organizer</h1>
<p>
	All tickets are sold by Event-Biller.com, which acts as an intermediate.
	Then, Event-Biller.com transfers the funds corresponding to the sold tickets
	to the organizer except the service fees.
	The transfer are regularely done according a period of time defined between the
	organizer and Event-Biller.com.
	Event-Biller.com cannot be responsible or liable for external problems
	regarding fund transfers that may cause delayed transfer or no transfer at all.
</p>

