<h1>Création d'évènement</h1>
<p>
	Lorsqu'un utilisateur (agissant en tant qu'organisateur d'évènement) crée un évènement,
	il doit d'abord être identifié.
	L'utilisateur spécifie quand l'évènement aura lieu (la date de la manifestation),
	quand l'évènement sera confirmé ou annulé au plus tard (date de confirmation).

	L'organisateur indique la condition financière à remplir avant la date de confirmation
	pour laisser Event-Biller.com confirmer ou annuler l'évènement.

	Un évènement est par défaut à l'état plannifié. Cela signifie que l'organisateur
	ne s'est pas encore engagé à le réaliser.

	Les participants ne peuvent que réserver les billets (et non les acheter).
	Si l'évènement est confirmé, il engage l'organisateur à le réaliser conformément à ce qu'il
	avait indiqué.

	Après qu'un évènement est confirmé, toute plainte ou réclamation des participants doivent
	être adressées directement à l'organisateur et en aucun cas à Event-Biller.com.

</p>

