<h1>Description générale</h1>
<p>
	L'objectif de event-biller.com est d'aider les personnes, organisations et
	entreprises à réduire les risques financiers lié à l'organisation d'évènements.

	En particulier event-biller.com essaie de réaliser un service de financement participatif
	pour évènement. Les organisateurs promouvoient leur projet d'évènement sur le site internet
	et si ils obtiennent un nombre de réservation correspondant à des fonds suffisants pour la
	bonne tenue de l'évènement alors l'évènement peut etre organisé sans risque lié à un manque
	de participants.
</p>
<h1>Description détaillée</h1>
<p>
	En accédant au site internet, un utilisateur peut consulter les évènements publiés
	ou créér son propre projet d'évènement.
</p>
<p>
	Le site internet est équipé d'un système de compte utilisateur, ce
	qui permet à ces derniers de naviguer tout en étant identifié au travers d'une
	session.
	La plupart des activités comme la création ou la gestion d'évènement, ou encore
	l'achat ou la réservation de billet implique que l'utilisateur soit identifié.
</p>
<p>
	Lors de la réservation d'un billet, l'utilisateur donne seulement l'autorisation d'un paiement.
	Le virement du paiement est effectué (capturé) seulement lorsque l'évènement est confirmé.
	Lors de l'achat d'un billet concernant un évènement confirmé, l'utilisateur donne une autorisation
