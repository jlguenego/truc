<h1>Confirmation ou Annulation d'évènement</h1>
<p>
	Un évènement peut etre confirmé ou annulé par Event-Biller.com à la date de confirmation
	si les conditions financières sont remplies.
	Il peut être annulé ou confirmé par l'organisateur avant la date de confirmation.

	Un évènement peut être confirmé ou annulé avant la date de confirmation.
	Si l'évènement est confirmé alors les billets réservés sont transformés en billets achetés,
	et les autorisations de prélèvement sont capturées.
	Si l'évènement est annulé alors tout les tickets réservés sont annulés, et parce que aucun
	paiement n'a été capturé, il n'y a aucun participant à rembourser.
</p>

