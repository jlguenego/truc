<h1>Financement participatif d'évènement</h1>
<p>
Le but d'Event-Biller est d'aider les gens à organiser des événements dont le coût ne peut être supporté par
l'organisateur, en terme de prise de risque.
Le risque de l'organisation d'un événement est d'avoir dépensé de l'argent
pour le réaliser sans avoir une garantie qu'il y ait assez de participants
pour équilibrer le budget.
</p>
<p>
Pour diminuer cette prise de risque, Event-Biller
propose aux organisateurs d'annoncer leurs projets d'événements sur son site web
et permet de réserver et d'acheter des billets en ligne.
Si suffisamment de personnes ont fait la réservation dans les temps impartis,
les organisateurs peuvent être sûrs d'avoir assez de fond pour financer l'événement.
</p>
