<h1>Crowdfunding events.</h1>
<p>
	The purpose of Event-Biller is to help people organizing events with significant charges.
	The risk organizing an event is to have spent money for making it and not be able
	to recover due to not enough people coming.
</p>
<p>
	To avoid this situation, Event-Biller
	proposes to the organizers to advertise their event projects on its web site
	and allows tickets to be booked or sold. If enough people made booking in time,
	organizers can be sure to have enough finance to do the event.
</p>
