<h1>Quelles sont les informations que nous recueillons ?</h1>
<p>
Nous recueillons des informations lorsque vous vous inscrivez sur notre site,
passer une commande,
vous vous abonnez à notre lettre d'information,
répondez à un sondage ou remplissez un formulaire.
</p>
<p>
Lors de l'utilisation de nos services à travers le site internet,
vous pouvez être invité à entrer vos: nom, adresse e-mail, adresse postale ou numéro de téléphone. Vous pouvez, toutefois, visitez notre site de façon anonyme.
</p>
<p>
Google, comme un fournisseur tiers,
utilise des cookies pour diffuser des annonces sur notre site.
L'utilisation par Google du cookie DART lui permet de diffuser des annonces
aux utilisateurs en fonction de leur visite à vos sites et d'autres sites sur Internet.
Les utilisateurs peuvent désactiver l'utilisation du cookie DART (voir avec Google).
</p>

<h1>Comment utilisons nous vos informations ?</h1>
<p>
Toute les informations que nous recueillons auprès de vous peuvent être utilisés
de l'une des façons suivantes:
</p>

<ul>
<li>Pour personnaliser votre expérience (Votre information nous aide à mieux répondre à vos besoins individuels)</li>

<li>Pour améliorer notre site web
(Nous nous efforçons continuellement d'améliorer nos offres web basées
 sur l'information et les commentaires que nous recevons de vous)
</li>
<li>Pour améliorer le service à la clientèle
(Votre information nous aide à répondre plus efficacement à vos demandes de service à la clientèle et les besoins de soutien)
</li>
<li>Pour traiter les transactions
Vos informations, qu'elles soient publiques ou privées, ne seront pas vendues, échangées,
transférées, ou données à une autre société pour quelque raison que ce soit,
sans votre consentement, sauf dans le but exprès de livrer
