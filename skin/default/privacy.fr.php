</li>
<li>Pour envoyer des courriels périodiques
L'adresse email que vous fournissez pour le traitement de la commande,
ne sera utilisée pour vous envoyer des informations et
mises à jour relatives à votre commande.
</li>
</ul>

<h1>Comment protégeons-nous vos informations ?</h1>
<p>
Nous mettons en œuvre une variété de mesures de sécurité pour maintenir la
sécurité de vos informations personnelles lorsque vous passez une commande
ou d'entrer, de soumettre, ou accéder à vos renseignements personnels.
</p>
<p>
Nous vous proposons l'utilisation d'un serveur sécurisé.
Toutes informations peuvent être transmises via Secure Socket Layer (SSL),
puis cryptés dans notre base de données fournisseurs
que pour être accessibles par les personnes autorisées avec les droits d'accès spéciaux
à ces systèmes, et sont tenues de garder l'information confidentielle.
</p>
<p>
Après une opération, vos informations personnelles de paiement
ne seront pas conservées au dossier pendant plus de 60 jours.
</p>

