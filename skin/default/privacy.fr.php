</p>
<p>
Devons-nous divulguer toute information à des tiers?
</p>
<p>
Nous ne vendons pas, ne commercialisons pas, ou ne transférons pas
à des tiers vos données personnelles.
Ceci n'inclut pas les tiers de confiance qui nous aident à exploiter
notre site Web, la conduite de nos affaires, ou vous servir,
